//Menyusun Barisan Bintang
console.log('Menyusun Barisan Bintang');
var rows1 = 5;

for (let i = 1; i <= rows1 ; i++) {
    console.log('*');
    
}

//Barisan Bintang Dengan Nested Looping
console.log('Barisan Bintang Dengan Nested Looping');
var rows2 = 5;

for(var i = 1; i <= rows2; i++){
    console.log("*".repeat(rows2));
 }


//Barisan Tangga Bintang Dengan Nested Looping
