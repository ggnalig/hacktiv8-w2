//2. ---------Algorithm---------- TAHUN KABISAT
//  - x adalah angka tahun yang di tes
//  - jika x habis di bagi 4 maka tahun itu tersebut kabisat
//  - jika x habis dibagi 100 dan habis dibagi 400 maka tahun tersebut kabisat
//  - jika x habis dibagi 4 dan tidak habis dibagi 100 maka tahun tersebut kabisat
//  ---------Pseudocode-----------
//  - SET tahun EQUALS TO x
//  - IF x modulus 400 EQUALS TO 0 THEN
//  - DISPLAY tahun x adalah tahun kabisat
//  - ELSE IF x modulus 100 EQUALS TO 0 THEN
//  - DISPLAY x adalah tahun kabisat
//  - IF ELSE x modulus 4 EQUALS TO 0 THEN
//  - DISPLAY x adalah tahun kabisat
//  - ELSE DISPLAY tahun x bukan tahun kabisat
//  - END IF

//3. ---------Algorithm---------- LAUNDRY DAY
//  - mesin baru dinyalakan setelah ada 20 baju di dalamnya
//  ---------Pseudocode-----------
//  - SET baju EQUALS TO i
//  - FOR ( SET i EQUALS TO 1, i < 21, i INCREMENT COUNTER)
//  - DISPLAY i
//  - IF i EQUALS TO 20 THEN
//  - DISPLAY mesin menyala
//  - ELSE DISPLAY jumlah baju baru sebanyak i belum mencapai 20
//  - END FOR

//4. ---------Algorithm---------- PERIKSA KUKU
