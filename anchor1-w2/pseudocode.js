//  - a adalah nilai percepatan
//  - ΣF adalah massa benda x percepatan
//  ---------Pseudocode-----------
//  - SET m EQUALS TO 600
//  - SET a EQUALS TO 2
//  - SET ΣF EQUALS TO m*a
